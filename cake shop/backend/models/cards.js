import mongoose from 'mongoose';

const cardSchema = new mongoose.Schema({
	name: {
		type: String,
		required: true,
	},
	description: {
		type: String,
		required: true,
	},
	price: {
		type: Number,
		required: true,
	},
	imageUrl: {
		type: String,
		required: true,
	},
});

const Cards = mongoose.model('Card', cardSchema);

export default Cards;
